/**
 * make-fixtures.mjs — regenerates synthetic test fixtures for all non-HEIC formats.
 *
 * Generates 64×64 gradient images in: JPEG, PNG, WebP, GIF, BMP, AVIF.
 * HEIC is NOT generated here — it requires libheif tooling not available
 * in all environments. The committed sample.heic was sourced from the
 * libheif project (https://github.com/strukturag/libheif) and is
 * redistributable under its original license for test purposes.
 *
 * Requires: Python 3 + Pillow, ffmpeg on PATH.
 *
 * Run:  node scripts/make-fixtures.mjs
 */

import { execSync } from 'child_process';
import { existsSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = resolve(__dirname, '../public/test-fixtures');

function run(cmd, label) {
  console.log(`[fixtures] ${label} ...`);
  try {
    execSync(cmd, { stdio: 'pipe' });
    console.log(`[fixtures] ${label} done`);
  } catch (err) {
    console.error(`[fixtures] ${label} FAILED:`, err.stderr?.toString() ?? err.message);
    process.exit(1);
  }
}

// ── Python/Pillow: JPEG, WebP, GIF, BMP ─────────────────────────────────────
const pythonScript = `
import os, sys
try:
    from PIL import Image, ImageDraw
except ImportError:
    sys.exit("Pillow not installed: pip install Pillow")

out = ${JSON.stringify(OUT)}
img = Image.new('RGB', (64, 64))
for x in range(64):
    for y in range(64):
        img.putpixel((x, y), (x * 4, y * 4, min(255, (x + y) * 2)))

img.save(os.path.join(out, 'sample.jpg'), 'JPEG', quality=85)
img.save(os.path.join(out, 'sample.webp'), 'WEBP', quality=80)
img.save(os.path.join(out, 'sample.gif'), 'GIF')
img.save(os.path.join(out, 'sample.bmp'), 'BMP')
print('PIL fixtures done')
`.trim();

run(`python3 -c "${pythonScript.replace(/"/g, '\\"')}"`, 'JPEG/WebP/GIF/BMP via Pillow');

// ── PNG: already exists from project setup; regenerate for consistency ────────
run(
  `python3 -c "
from PIL import Image
import os
img = Image.new('RGB', (64, 64))
for x in range(64):
    for y in range(64):
        img.putpixel((x, y), (x * 4, y * 4, min(255, (x + y) * 2)))
img.save('${OUT}/sample.png', 'PNG')
print('PNG done')
"`,
  'PNG via Pillow'
);

// ── AVIF: via ffmpeg (libaom-av1) ────────────────────────────────────────────
if (!existsSync('/usr/local/bin/ffmpeg') && !existsSync('/home/radgh/.local/bin/ffmpeg')) {
  console.warn('[fixtures] WARNING: ffmpeg not found — sample.avif will not be regenerated.');
} else {
  run(
    `ffmpeg -y -f lavfi -i "testsrc=size=64x64:rate=1" -frames:v 1 "${OUT}/sample.avif" 2>/dev/null`,
    'AVIF via ffmpeg'
  );
}

console.log('[fixtures] All done. Files in', OUT);
console.log('[fixtures] NOTE: sample.heic is pre-committed and not regenerated by this script.');
